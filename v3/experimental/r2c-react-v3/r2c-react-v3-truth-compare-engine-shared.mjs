function chengStr(text) {
  return `"${String(text || '').replace(/\\/g, '\\\\').replace(/"/g, '\\"').replace(/\n/g, '\\n').replace(/\t/g, '\\t')}"`;
}

function renderTruthCompareMainCheng(compareModule) {
  return [
    `import ${compareModule} as truth_compare`,
    '',
    'fn main(): int32 =',
    '    truth_compare.emit()',
    '    return 0',
    '',
  ].join('\n');
}

