function renderTruthCompareEngineCheng(execIoModule, dataModule) {
  return [
    `import ${execIoModule} as exec_io`,
    `import ${dataModule} as data`,
    '',
    'fn emitBoolIntJson(value: int32) =',
    '    if value != 0:',
    '        exec_io.r2cWriteStdout("true")',
    '        return',
    '    exec_io.r2cWriteStdout("false")',
    '    return',
    '',
    'fn emitDigitJson(value: int32) =',
    '    if value == 0:',
    '        exec_io.r2cWriteStdout("0")',
    '        return',
    '    if value == 1:',
    '        exec_io.r2cWriteStdout("1")',
    '        return',
    '    if value == 2:',
    '        exec_io.r2cWriteStdout("2")',
    '        return',
    '    if value == 3:',
    '        exec_io.r2cWriteStdout("3")',
    '        return',
    '    if value == 4:',
    '        exec_io.r2cWriteStdout("4")',
    '        return',
    '    if value == 5:',
    '        exec_io.r2cWriteStdout("5")',
    '        return',
    '    if value == 6:',
    '        exec_io.r2cWriteStdout("6")',
    '        return',
    '    if value == 7:',
    '        exec_io.r2cWriteStdout("7")',
    '        return',
    '    if value == 8:',
    '        exec_io.r2cWriteStdout("8")',
    '        return',
    '    exec_io.r2cWriteStdout("9")',
    '    return',
    '',
    'fn emitPositiveIntJson(value: int32) =',
    '    if value >= 10:',
    '        emitPositiveIntJson(value / 10)',
    '    emitDigitJson(value % 10)',
    '    return',
    '',
    'fn emitIntJson(value: int32) =',
    '    if value < 0:',
    '        exec_io.r2cWriteStdout("-")',
    '        emitPositiveIntJson(0 - value)',
    '        return',
    '    emitPositiveIntJson(value)',
    '    return',
    '',
    'fn emitReasonJson(reasonCode: int32) =',
    '    if reasonCode == 1:',
    `        exec_io.r2cWriteStdout(${chengStr(JSON.stringify('missing_truth_state'))})`,
    '        return',
    '    if reasonCode == 2:',
    `        exec_io.r2cWriteStdout(${chengStr(JSON.stringify('missing_truth_fields'))})`,
    '        return',
    '    if reasonCode == 3:',
    `        exec_io.r2cWriteStdout(${chengStr(JSON.stringify('mismatch'))})`,
    '        return',
    `    exec_io.r2cWriteStdout(${chengStr(JSON.stringify('ok'))})`,
    '    return',
    '',
    'fn truthFindMatchedSnapshotIndex(targetState: str): int32 =',
    '    var index: int32',
    '    while index < data.truthSnapshotCount():',
    '        if data.truthSnapshotStateId(index) == targetState:',
    '            return index',
    '        if data.truthSnapshotStateId(index) == "" && data.truthSnapshotRouteId(index) == targetState:',
    '            return index',
    '        index = index + 1',
    '    return -1',
    '',
    'fn missingTruthFieldCount(index: int32): int32 =',
    '    var count: int32',
    '    if data.truthSnapshotStateId(index) == "":',
    '        count = count + 1',
    '    if data.truthSnapshotRenderReadyInt(index) < 0:',
    '        count = count + 1',
    '    if data.truthSnapshotSemanticNodesLoadedInt(index) < 0:',
    '        count = count + 1',
    '    if data.truthSnapshotSemanticNodesCountInt(index) < 0:',
    '        count = count + 1',
    '    return count',
    '',
    'fn mismatchCount(index: int32): int32 =',
    '    var count: int32',
    '    if data.truthSnapshotStateId(index) != "" && data.execRouteState() != data.truthSnapshotStateId(index):',
    '        count = count + 1',
    '    if data.truthSnapshotRenderReadyInt(index) >= 0:',
    '        if data.execRenderReadyInt() != data.truthSnapshotRenderReadyInt(index):',
    '            count = count + 1',
    '    if data.truthSnapshotSemanticNodesLoadedInt(index) >= 0:',
    '        if data.execSemanticNodesLoadedInt() != data.truthSnapshotSemanticNodesLoadedInt(index):',
    '            count = count + 1',
    '    if data.truthSnapshotSemanticNodesCountInt(index) >= 0:',
    '        if data.execSemanticNodesCount() != data.truthSnapshotSemanticNodesCountInt(index):',
    '            count = count + 1',
    '    return count',
    '',
    'fn compareReasonCode(matchedIndex: int32): int32 =',
    '    if matchedIndex < 0:',
    '        return 1',
    '    if missingTruthFieldCount(matchedIndex) > 0:',
    '        return 2',
    '    if mismatchCount(matchedIndex) > 0:',
    '        return 3',
    '    return 0',
    '',
    'fn emitMissingTruthFields(matchedIndex: int32) =',
    '    exec_io.r2cWriteStdout("[")',
    '    var wrote: int32',
    '    if data.truthSnapshotStateId(matchedIndex) == "":',
    '        exec_io.r2cWriteStdout("\\"route_state\\"")',
    '        wrote = 1',
    '    if data.truthSnapshotRenderReadyInt(matchedIndex) < 0:',
    '        if wrote != 0:',
    '            exec_io.r2cWriteStdout(",")',
    '        exec_io.r2cWriteStdout("\\"render_ready\\"")',
    '        wrote = 1',
    '    if data.truthSnapshotSemanticNodesLoadedInt(matchedIndex) < 0:',
    '        if wrote != 0:',
    '            exec_io.r2cWriteStdout(",")',
    '        exec_io.r2cWriteStdout("\\"semantic_nodes_loaded\\"")',
    '        wrote = 1',
    '    if data.truthSnapshotSemanticNodesCountInt(matchedIndex) < 0:',
    '        if wrote != 0:',
    '            exec_io.r2cWriteStdout(",")',
    '        exec_io.r2cWriteStdout("\\"semantic_nodes_count\\"")',
    '    exec_io.r2cWriteStdout("]")',
    '    return',
    '',
    'fn emitMismatches(matchedIndex: int32) =',
    '    exec_io.r2cWriteStdout("[")',
    '    var wrote: int32',
    '    if data.truthSnapshotStateId(matchedIndex) != "" && data.execRouteState() != data.truthSnapshotStateId(matchedIndex):',
    '        exec_io.r2cWriteStdout("{\\"field\\":\\"route_state\\",\\"exec_value\\":")',
    '        data.emitExecRouteStateCompareJson()',
    '        exec_io.r2cWriteStdout(",\\"truth_value\\":")',
    '        data.emitTruthSnapshotRouteStateCompareJson(matchedIndex)',
    '        exec_io.r2cWriteStdout("}")',
    '        wrote = 1',
    '    if data.truthSnapshotRenderReadyInt(matchedIndex) >= 0:',
    '        if data.execRenderReadyInt() != data.truthSnapshotRenderReadyInt(matchedIndex):',
    '            if wrote != 0:',
    '                exec_io.r2cWriteStdout(",")',
    '            exec_io.r2cWriteStdout("{\\"field\\":\\"render_ready\\",\\"exec_value\\":")',
    '            data.emitExecRenderReadyCompareJson()',
    '            exec_io.r2cWriteStdout(",\\"truth_value\\":")',
    '            data.emitTruthSnapshotRenderReadyCompareJson(matchedIndex)',
    '            exec_io.r2cWriteStdout("}")',
    '            wrote = 1',
    '    if data.truthSnapshotSemanticNodesLoadedInt(matchedIndex) >= 0:',
    '        if data.execSemanticNodesLoadedInt() != data.truthSnapshotSemanticNodesLoadedInt(matchedIndex):',
    '            if wrote != 0:',
    '                exec_io.r2cWriteStdout(",")',
    '            exec_io.r2cWriteStdout("{\\"field\\":\\"semantic_nodes_loaded\\",\\"exec_value\\":")',
    '            data.emitExecSemanticNodesLoadedCompareJson()',
    '            exec_io.r2cWriteStdout(",\\"truth_value\\":")',
    '            data.emitTruthSnapshotSemanticNodesLoadedCompareJson(matchedIndex)',
    '            exec_io.r2cWriteStdout("}")',
    '            wrote = 1',
    '    if data.truthSnapshotSemanticNodesCountInt(matchedIndex) >= 0:',
    '        if data.execSemanticNodesCount() != data.truthSnapshotSemanticNodesCountInt(matchedIndex):',
    '            if wrote != 0:',
    '                exec_io.r2cWriteStdout(",")',
    '            exec_io.r2cWriteStdout("{\\"field\\":\\"semantic_nodes_count\\",\\"exec_value\\":")',
    '            data.emitExecSemanticNodesCountCompareJson()',
    '            exec_io.r2cWriteStdout(",\\"truth_value\\":")',
    '            data.emitTruthSnapshotSemanticNodesCountCompareJson(matchedIndex)',
    '            exec_io.r2cWriteStdout("}")',
    '    exec_io.r2cWriteStdout("]")',
    '    return',
    '',
    'fn emitJson() =',
    '    var matchedIndex: int32 = truthFindMatchedSnapshotIndex(data.truthState())',
    '    var reasonCode: int32 = compareReasonCode(matchedIndex)',
    '    exec_io.r2cWriteStdout("{\\"format\\":\\"truth_compare_v1\\"")',
    '    exec_io.r2cWriteStdout(",\\"ok\\":")',
    '    if reasonCode == 0:',
    '        exec_io.r2cWriteStdout("true")',
    '    else:',
    '        exec_io.r2cWriteStdout("false")',
    '    exec_io.r2cWriteStdout(",\\"reason\\":")',
    '    emitReasonJson(reasonCode)',
    '    exec_io.r2cWriteStdout(",\\"truth_trace_path\\":")',
    '    data.emitTruthTracePathJson()',
    '    exec_io.r2cWriteStdout(",\\"truth_trace_format\\":")',
    '    data.emitTruthTraceFormatJson()',
    '    exec_io.r2cWriteStdout(",\\"truth_state\\":")',
    '    data.emitTruthStateJson()',
    '    exec_io.r2cWriteStdout(",\\"matched_snapshot_index\\":")',
    '    emitIntJson(matchedIndex)',
    '    exec_io.r2cWriteStdout(",\\"exec_snapshot_path\\":")',
    '    data.emitExecSnapshotPathJson()',
    '    exec_io.r2cWriteStdout(",\\"out_path\\":")',
    '    data.emitOutPathJson()',
    '    exec_io.r2cWriteStdout(",\\"exec_route_state\\":")',
    '    data.emitExecRouteStateJson()',
    '    exec_io.r2cWriteStdout(",\\"exec_render_ready\\":")',
    '    emitBoolIntJson(data.execRenderReadyInt())',
    '    exec_io.r2cWriteStdout(",\\"exec_semantic_nodes_loaded\\":")',
    '    emitBoolIntJson(data.execSemanticNodesLoadedInt())',
    '    exec_io.r2cWriteStdout(",\\"exec_semantic_nodes_count\\":")',
    '    emitIntJson(data.execSemanticNodesCount())',
    '    exec_io.r2cWriteStdout(",\\"truth_route_state\\":")',
    '    if matchedIndex >= 0:',
    '        data.emitTruthSnapshotRouteStateJson(matchedIndex)',
    '    else:',
    `        exec_io.r2cWriteStdout(${chengStr(JSON.stringify(''))})`,
    '    exec_io.r2cWriteStdout(",\\"truth_route_id\\":")',
    '    if matchedIndex >= 0:',
    '        data.emitTruthSnapshotRouteIdJson(matchedIndex)',
    '    else:',
    `        exec_io.r2cWriteStdout(${chengStr(JSON.stringify(''))})`,
    '    exec_io.r2cWriteStdout(",\\"truth_render_ready\\":")',
    '    if matchedIndex >= 0:',
    '        if data.truthSnapshotRenderReadyInt(matchedIndex) >= 0:',
    '            emitBoolIntJson(data.truthSnapshotRenderReadyInt(matchedIndex))',
    '        else:',
    '            exec_io.r2cWriteStdout("false")',
    '    else:',
    '        exec_io.r2cWriteStdout("false")',
    '    exec_io.r2cWriteStdout(",\\"truth_semantic_nodes_loaded\\":")',
    '    if matchedIndex >= 0:',
    '        if data.truthSnapshotSemanticNodesLoadedInt(matchedIndex) >= 0:',
    '            emitBoolIntJson(data.truthSnapshotSemanticNodesLoadedInt(matchedIndex))',
    '        else:',
    '            exec_io.r2cWriteStdout("false")',
    '    else:',
    '        exec_io.r2cWriteStdout("false")',
    '    exec_io.r2cWriteStdout(",\\"truth_semantic_nodes_count\\":")',
    '    if matchedIndex >= 0:',
    '        if data.truthSnapshotSemanticNodesCountInt(matchedIndex) >= 0:',
    '            data.emitTruthSnapshotSemanticNodesCountJson(matchedIndex)',
    '        else:',
    '            exec_io.r2cWriteStdout("0")',
    '    else:',
    '        exec_io.r2cWriteStdout("0")',
    '    exec_io.r2cWriteStdout(",\\"missing_truth_fields\\":")',
    '    if matchedIndex >= 0:',
    '        emitMissingTruthFields(matchedIndex)',
    '    else:',
    '        exec_io.r2cWriteStdout("[]")',
    '    exec_io.r2cWriteStdout(",\\"mismatches\\":")',
    '    if matchedIndex >= 0:',
    '        emitMismatches(matchedIndex)',
    '    else:',
    '        exec_io.r2cWriteStdout("[]")',
    '    exec_io.r2cWriteStdout("}")',
    '    return',
    '',
    'fn emit() =',
    '    emitJson()',
    '    exec_io.r2cWriteStdout("\\n")',
    '    return',
    '',
  ].join('\n');
}

